
function goToPage(emotion) {
    if (emotion === 'tristeza') {
        window.location.href = './assets/pagesHome/tristeza.html';
    } else if (emotion === 'medo') {
        window.location.href = './assets/pagesHome/medo.html';
    } else if (emotion === 'raiva') {
        window.location.href = './assets/pagesHome/raiva.html';
    }
    else if (emotion === 'ansiedade') {
        window.location.href = './assets/pagesHome/ansiedade.html';
    }
}

